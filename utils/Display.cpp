void Display::showHeader(const std::string& title) {
    std::cout << "\r\n  " << std::string(50, '=') << "\r\n";
    std::cout << "   " << title << "\r\n";
    std::cout << "  " << std::string(50, '=') << "\r\n";
}

void Display::showSubHeader(const std::string& title) {
    std::cout << "\r\n  " << std::string(40, '-') << "\r\n";
    std::cout << "   " << title << "\r\n";
    std::cout << "  " << std::string(40, '-') << "\r\n";
}

void Display::showDivider() {
    std::cout << "  " << std::string(50, '-') << "\r\n";
}

void Display::showSuccess(const std::string& message) {
    std::cout << "  \xE2\x9C\x85 " << message << "\r\n";
}

void Display::showError(const std::string& message) {
    std::cout << "  \xE2\x9D\x8C " << message << "\r\n";
}

void Display::showWarning(const std::string& message) {
    std::cout << "  \xE2\x9A\xA0\xEF\xB8\x8F  " << message << "\r\n";
}

void Display::showInfo(const std::string& message) {
    std::cout << "  \xE2\x84\xB9\xEF\xB8\x8F  " << message << "\r\n";
}

void Display::pressEnterToContinue() {
    std::cout << "\r\n  Press Enter to continue...";
    std::string dummy;
    std::getline(std::cin, dummy);
}

void Display::clearScreen() {
    system("cls");
}

